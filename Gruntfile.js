module.exports = function (grunt) {

    var photoSize = 262; // 262px square
    var badgeSize = 90;

    // Project configuration.
    grunt.initConfig({

        // Get package information
        pkg: grunt.file.readJSON('package.json'),

        clean: {
            dist: {
                src: ['dist']
            },
            tmp: {
                src: ['tmp']
            }
        },

        jshint: {
            gruntfile: {
                src: ['Gruntfile.js']
            },
            dist: {
                src: ['src/js/*.js']
            }
        },

        copy: {
            dist: {
                cwd: 'src',
                src: ['**', '.htaccess', '!index.html.tpl', '!img/**', '!css/**', '!js/**'],
                dest: 'dist',
                expand: true
            }
        },

        uglify: {
            dist: {
                src: ['src/js/**/*.js'],
                dest: 'tmp/script.min.js',
                options: {
                    preserveComments: false
                }
            }
        },

        // Compass compile for css
        less: {
            dist: {
                options: {
                    strictMath: true,
                    paths: ['node_modules/bootstrap/less', 'src/css']
                },
                files: {
                    'tmp/compiled.css': 'src/css/style.less'
                }
            }
        },

        sprite: {
            dist: {
                src: 'tmp/backpack/images/*.*',
                dest: 'dist/spritesheet.png',
                destCss: 'tmp/spritesheet.css',
                imgPath: 'spritesheet.png',
                algorithmOpts: {sort: false}
            }
        },

        uncss: {
            dist: {
                files: {
                    'tmp/tidy.css': 'tmp/index1.html'
                }
            }
        },

        cssmin: {
            dist: {
                files: {
                    'tmp/minified.css': 'tmp/tidy.css',
                    'dist/spritesheet.css': 'tmp/spritesheet.css'
                }
            }
        },

        image_resize: {
            lossless: {
                options: {
                    width: photoSize,
                    height: photoSize,
                    quality: 1.0
                },
                files: {
                    'dist/photo.jpg': 'src/img/photo.jpg'
                }
            },
            compressed: {
                options: {
                    width: photoSize * 0.25,
                    height: photoSize * 0.25,
                    quality: 0.0
                },
                files: {
                    'tmp/photo.jpg': 'src/img/photo.jpg'
                }
            },
            backpack: {
                options: {
                    width: badgeSize,
                    height: badgeSize,
                    quality: 1.0,
                    crop: true
                },
                src: 'src/img/backpack/*.*',
                dest: 'tmp/backpack/images/'
            }
        },

        base64: {
            dist: {
                files: {
                    'tmp/photo.b64': 'tmp/photo.jpg'
                }
            }
        },

        // Fetch data from APIs
        curl: {
            backpackGroups: {
                src: {
                    url: 'https://backpack.openbadges.org/displayer/274141/groups.json',
                    method: 'GET'
                },
                dest: 'tmp/backpack/groups.json'
            }
        },

        template: {
            dist: {
                options: {
                    data: function () {

                        var data = {
                            photo: grunt.file.read('tmp/photo.b64'),
                            photoSize: photoSize
                        };

                        return data;
                    }
                },
                files: {
                    'tmp/index1.html': ['src/index.html.tpl']
                }
            }
        },

        processhtml: {
            dist: {
                files: {
                    'tmp/index2.html': ['tmp/index1.html']
                }
            }
        },

        // Compress HTML
        htmlmin: {
            dist: {
                options: {
                    removeComments: true,
                    collapseWhitespace: true
                },
                files: {
                    'dist/index.html': 'tmp/index2.html'
                }
            }
        },

        // Watch tasks
        watch: {

            // If this file changes we need to reloaded
            // Furthermore it might be necessary to rebuild files that are generated by settings in here
            gruntfile: {
                files: ['Gruntfile.js'],
                tasks: ['jshint:gruntfile'],
                options: {
                    reload: true
                }
            },

            // Validate and compress Javascript on change
            js: {
                files: ['src/js/**/*.js'],
                tasks: ['build:html+css+js'],
                options: {
                    interrupt: true,
                    livereload: true
                }
            },

            // Compile CSS on change
            css: {
                files: 'src/css/**/*.less',
                tasks: ['build:html+css+js'],
                options: {
                    interrupt: true,
                    livereload: true
                }
            },

            // Compile and compress HTML on change
            html: {
                files: 'src/index.html.tpl',
                tasks: ['build:html+css+js'],
                options: {
                    interrupt: true,
                    livereload: true
                }
            },

            other: {
                files: ['src/**', 'src/.htaccess', '!src/index.html.tpl', '!src/js/**', '!src/css/**', '!src/img/**'],
                tasks: ['copy'],
                options: {
                    interrupt: true,
                    livereload: true
                }
            }
        }

    });

    // Load the plugins
    require('load-grunt-tasks')(grunt);

    grunt.registerTask('curl:backpackBadges',
        'Download badges for each collection',
        function () {

            var groups = grunt.file.readJSON('tmp/backpack/groups.json').groups;

            var config = {
                curl: {}
            };
            var tasks = [];

            groups.forEach(function (group, i) {

                var id = group.groupId;
                var taskName = 'backpackBadges' + id;
                var url = 'https://backpack.openbadges.org/displayer/274141/group/' + id + '.json';

                config.curl[taskName] = {
                    src: {
                        url: url,
                        method: 'GET',
                    },
                    dest: 'tmp/backpack/groups/' + id + '.json'
                };

                tasks.push('curl:' + taskName);
            });

            grunt.config.merge(config);
            grunt.task.run(tasks);
        }
    );

    grunt.registerTask('curl:backpackImages',
        '', function () {

            return; // Included in repo now

            var path = require('path');
            var fs = require('fs');
            var slug = require('slug');

            var groups = fs.readdirSync('tmp/backpack/groups');

            var config = {
                curl: {}
            };
            var tasks = [];

            groups.forEach(function (group) {

                var badges = grunt.file.readJSON('tmp/backpack/groups/' + group).badges;

                badges.forEach(function (badge) {

                    badge = badge.assertion.badge;
                    var name = slug(badge.name.replace(/\s*completed?\s*/i, '').toLowerCase());

                    var taskName = 'backpackImages-' + name;
                    var url = badge.image;
                    var ext = path.extname(url);

                    config.curl[taskName] = {
                        src: {
                            url: url,
                            method: 'GET',
                        },
                        dest: 'src/img/backpack/' + name + ext
                    };

                    tasks.push('curl:' + taskName);
                });
            });

            grunt.config.merge(config);
            grunt.task.run(tasks);
        }
    );

    grunt.registerTask(
        'build:html+css+js',
        'Compile template to HTML and compress',
        function () {

            grunt.task.run([

                'template:dist',

                'less:dist',
                'uncss:dist',
                'cssmin:dist',

                'uglify:dist',

                'processhtml:dist',
                'htmlmin:dist'
            ]);
        }
    );

    grunt.registerTask(
        'build:backpack',
        'Download and resize badges',
        function () {

            if (!grunt.file.exists('tmp/github/groups.json')) {
                grunt.task.run('curl:backpackGroups');
            }

            if (!grunt.file.exists('tmp/backpack/groups/')) {
                grunt.task.run('curl:backpackBadges');
            }

            if (!grunt.file.exists('tmp/backpack/images/')) {
                grunt.task.run('curl:backpackImages');
            }

            grunt.task.run('image_resize:badges');
        }
    );

    grunt.registerTask(
        'build:img',
        'Resize and compress images',
        ['image_resize:lossless', 'image_resize:compressed', 'base64:dist']
    );

    grunt.registerTask(
        'build:backpack',
        '',
        ['image_resize:backpack', 'sprite:dist',]
    );

    grunt.registerTask(
        'build',
        'Make a clean build',
        ['clean:dist', 'clean:tmp', 'build:img', 'build:backpack', 'build:html+css+js', 'copy:dist']
    );

};
